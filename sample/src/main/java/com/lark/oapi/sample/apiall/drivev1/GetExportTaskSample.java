package com.lark.oapi.sample.apiall.drivev1;

import com.lark.oapi.Client;
import com.lark.oapi.core.utils.Jsons;
import com.lark.oapi.service.drive.v1.model.GetExportTaskReq;
import com.lark.oapi.service.drive.v1.model.GetExportTaskResp;

// HTTP PATH: /open-apis/drive/v1/export_tasks/:ticket"
public class GetExportTaskSample {

  public static void main(String arg[]) throws Exception {
    // 构建client
    Client client = Client.newBuilder("appId", "appSecret").build();

    // 创建请求对象
    GetExportTaskReq req = GetExportTaskReq.newBuilder()
        .ticket("6933093124755423251")
        .token("doccnZVxxxxxxxxxxxxGiyBgYqe")
        .build();

    // 发起请求
    GetExportTaskResp resp = client.drive().exportTask().get(req);

    // 处理服务端错误
    if (!resp.success()) {
      System.out.println(String.format("code:%s,msg:%s,reqId:%s"
          , resp.getCode(), resp.getMsg(), resp.getRequestId()));
      return;
    }

    // 业务数据处理
    System.out.println(Jsons.DEFAULT.toJson(resp.getData()));
  }
}
